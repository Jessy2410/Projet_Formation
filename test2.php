    <script>
        document.getElementById('searchBar').addEventListener('input', function () {
            let filter = this.value.toLowerCase();
            let cards = document.querySelectorAll('#catalogue .col');

            cards.forEach(card => {
                let title = card.querySelector('.card-title').textContent.toLowerCase();
                if (title.includes(filter)) {
                    card.style.display = "block";
                } else {
                    card.style.display = "none";
                }
            });
        });

        document.addEventListener("DOMContentLoaded", function () {
            let searchBar = document.getElementById("searchBar");
            let container = document.querySelector(".container-elements");
            let cards = document.querySelectorAll(".container-elements .card");

            searchBar.addEventListener("input", function () {
                let filter = searchBar.value.trim().toLowerCase();
                let visibleCards = 0;

                cards.forEach(card => {
                    let title = card.querySelector(".card-title").textContent.toLowerCase();
                    if (title.includes(filter)) {
                        card.style.display = "block";
                        visibleCards++;
                    } else {
                        card.style.display = "none";
                    }
                });

                // Ajuster la disposition selon le nombre de résultats trouvés
                if (visibleCards <= 2) {
                    container.classList.add("few-results");
                    container.classList.remove("normal");
                } else {
                    container.classList.add("normal");
                    container.classList.remove("few-results");
                }

                // Si aucun résultat, revenir à la disposition normale
                if (filter.length === 0) {
                    container.classList.remove("few-results");
                    container.classList.add("normal");
                    cards.forEach(card => {
                        card.style.display = "block"; // Réafficher toutes les cartes
                    });
                }
            });
        });



        document.addEventListener("DOMContentLoaded", function() {
            const btnArrow = document.querySelector(".btn-arrow");
            const containerElements = document.querySelector(".container-elements");

            if (btnArrow && containerElements) {
                btnArrow.addEventListener("click", function() {
                    containerElements.scrollBy({ top: 470, behavior: "smooth" }); // Défile vers le bas de 470px
                });
            }
        });

